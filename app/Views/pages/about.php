    <h2>Our Story</h2>
    <p>VillaStudio was born from a simple yet powerful idea: to create a legal, high-quality, and community-driven platform for discovering and enjoying films from around the world. We saw a gap in the market for a service that not only respected copyright but also celebrated the work of independent filmmakers, especially from vibrant cinematic hubs like Nollywood and across Africa.</p>
    
    <h2>What We Do</h2>
    <p>We are more than just a streaming service. VillaStudio is an ecosystem for film lovers and creators. We provide:</p>
    <ul>
        <li>A curated library of licensed independent films, timeless public domain classics, and original content.</li>
        <li>A powerful platform for creators to upload, manage, and monetize their work.</li>
        <li>A vibrant community space for reviews, discussions, and shared viewing experiences.</li>
        <li>Seamless, high-quality streaming on any device, from your phone to your TV.</li>
    </ul>

    <h2>Our Vision</h2>
    <p>Our vision is to become the premier global destination for independent and culturally diverse cinema. We believe in the power of storytelling to connect people, and we are committed to building a sustainable and ethical platform that benefits viewers, creators, and the film industry as a whole.</p>
